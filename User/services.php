            <p><strong>Light Requirements:</strong> Plants vary in their light needs. Direct light means sunlight directly hits the plant, while indirect light means the plant is near but not in the path of the sun. Low-light plants can tolerate less light, making them suitable for rooms with little natural light.</p>
            <p><strong>Rotate Plants:</strong> Rotating plants ensures even growth. Without rotation, plants might lean toward the light source and become lopsided.</p>
