            <h3>Watering</h3>
            <p><strong>Regular Schedule:</strong> Establish a watering routine, but be flexible. Different plants have different water needs. For example, succulents need to dry out completely between waterings, whereas ferns prefer consistently moist soil.</p>
            <p><strong>Check the Soil:</strong> Stick your finger into the soil up to the first knuckle. If it feels dry, it's time to water. Overwatering can lead to root rot, a common issue where roots sit in too much water and start to decay.</p>
            <p><strong>Drainage:</strong> Ensure pots have drainage holes to allow excess water to escape. Use a saucer to catch water that drains out to avoid damaging surfaces.</p>
